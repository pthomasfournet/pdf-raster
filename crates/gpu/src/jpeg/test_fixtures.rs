/// Compile-time guard against accidental edits.  If this fixture ever
/// changes shape, every dependent test will silently start using a
/// different stream — block that at compile time.
const _: () = assert!(GRAY_16X16_JPEG.len() == 161);

/// Minimal SOF2 (progressive) stream — used by parsers that should *not*
/// silently accept progressive JPEG.
#[rustfmt::skip]
